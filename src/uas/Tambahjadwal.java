    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            Connection conn = Koneksi.getConnection();
            String sql = "INSERT INTO jadwal (kursus_id, instruktur_id, hari, jam, ruangan) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            
            String kursusItem = cmbKursus.getSelectedItem().toString();
            String[] partsK = kursusItem.split(" - ");
            pst.setString(1, partsK[0]);
            
            String instrukturItem = cmbInstruktur.getSelectedItem().toString();
            String[] partsI = instrukturItem.split(" - ");
            pst.setString(2, partsI[0]);
            
            pst.setString(3, cmbHari.getSelectedItem().toString());
            pst.setString(4, cmbJam.getSelectedItem().toString());
            pst.setString(5, cmbRuangan.getSelectedItem().toString());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Jadwal Berhasil Disimpan!");
            Dashboard.instance.switchPanel(new Jadwal());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

