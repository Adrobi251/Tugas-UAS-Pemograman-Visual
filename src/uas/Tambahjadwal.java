    private void loadComboBoxes() {
        cmbKursus.removeAllItems();
        cmbInstruktur.removeAllItems();
        
        cmbHari.removeAllItems();
        cmbHari.addItem("Senin"); cmbHari.addItem("Selasa"); cmbHari.addItem("Rabu");
        cmbHari.addItem("Kamis"); cmbHari.addItem("Jumat"); cmbHari.addItem("Sabtu");
        
        cmbJam.removeAllItems();
        cmbJam.addItem("08:00 - 10:00"); cmbJam.addItem("10:00 - 12:00");
        cmbJam.addItem("13:00 - 15:00"); cmbJam.addItem("15:00 - 17:00");
        
        cmbRuangan.removeAllItems();
        cmbRuangan.addItem("Lab Komputer 1"); cmbRuangan.addItem("Lab Komputer 2");
        cmbRuangan.addItem("Ruang Teori A");
        
        try {
            Connection conn = Koneksi.getConnection();
            Statement st = conn.createStatement();
            ResultSet rsK = st.executeQuery("SELECT id, nama_kursus FROM kursus");
            while (rsK.next()) {
                cmbKursus.addItem(rsK.getString("id") + " - " + rsK.getString("nama_kursus"));
            }
            
            ResultSet rsI = st.executeQuery("SELECT id, nama FROM instruktur");
            while (rsI.next()) {
                cmbInstruktur.addItem(rsI.getString("id") + " - " + rsI.getString("nama"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

